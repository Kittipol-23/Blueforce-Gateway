document.addEventListener('DOMContentLoaded', () => {
  // Theme Toggle Logic
  const themeToggleInfo = document.getElementById('theme-toggle');
  let currentTheme = localStorage.getItem('theme') || 'light';

  function applyTheme(theme) {
    if (theme === 'dark') {
      document.documentElement.classList.add('dark-mode');
    } else {
      document.documentElement.classList.remove('dark-mode');
    }

    if (themeToggleInfo) {
      if (theme === 'dark') {
        themeToggleInfo.innerHTML = '<svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="5"/><line x1="12" y1="1" x2="12" y2="3"/><line x1="12" y1="21" x2="12" y2="23"/><line x1="4.22" y1="4.22" x2="5.64" y2="5.64"/><line x1="18.36" y1="18.36" x2="19.78" y2="19.78"/><line x1="1" y1="12" x2="3" y2="12"/><line x1="21" y1="12" x2="23" y2="12"/><line x1="4.22" y1="19.78" x2="5.64" y2="18.36"/><line x1="18.36" y1="5.64" x2="19.78" y2="4.22"/></svg>';
      } else {
        themeToggleInfo.innerHTML = '<svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z"/></svg>';
      }
    }
  }

  // Initial Theme Load
  let savedLang = localStorage.getItem('lang') || 'en';
  if(savedLang === 'ja') savedLang = 'en';
  applyTheme(currentTheme);

  if (themeToggleInfo) {
    themeToggleInfo.addEventListener('click', () => {
      currentTheme = currentTheme === 'light' ? 'dark' : 'light';
      localStorage.setItem('theme', currentTheme);
      applyTheme(currentTheme);
    });
  }

  // Language Toggle Logic
  const langBtns = document.querySelectorAll('.lang-btn');

  function setLanguage(lang) {
    // Update active class
    langBtns.forEach(btn => {
      if (btn.dataset.lang === lang) {
        btn.classList.add('active');
      } else {
        btn.classList.remove('active');
      }
    });

    // Update translations
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    elementsToTranslate.forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          // (Optional: handle placeholders if they had data-i18n)
        } else {
          el.textContent = translations[lang][key];
        }
      }
    });

    // Update theme toggle icon
    applyTheme(currentTheme);
  }

  // Click events for Language buttons
  langBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const selectedLang = btn.dataset.lang;
      localStorage.setItem('lang', selectedLang);
      setLanguage(selectedLang);
    });
  });

  // Apply initial language state
  setLanguage(savedLang);

  // Mouse move visual effect
  // Select all dynamic diagram elements
  const movableElements = document.querySelectorAll('.node, .tree, .subpage-details, .connector-right');
  let isMoving = false;

  window.addEventListener('mousemove', (e) => {
    if (!isMoving) {
      window.requestAnimationFrame(() => {
        const mouseX = e.clientX;
        const mouseY = e.clientY;
        const centerX = window.innerWidth / 2;
        const centerY = window.innerHeight / 2;

        const percentX = (mouseX - centerX) / centerX;
        const percentY = (mouseY - centerY) / centerY;

        movableElements.forEach((el, index) => {
          let depth = 10;
          if (el.classList.contains('page-header')) depth = 5;
          if (el.classList.contains('node')) depth = 15;
          if (el.classList.contains('subpage-details')) depth = 12;
          if (el.classList.contains('connector-right')) depth = 8;
          if (el.classList.contains('tree')) depth = 20;

          const modDepthX = depth + (index % 3) * 2;
          const modDepthY = depth - (index % 2) * 2;

          const moveX = percentX * modDepthX;
          const moveY = percentY * modDepthY;

          el.style.setProperty('--mouse-x', `${moveX}px`);
          el.style.setProperty('--mouse-y', `${moveY}px`);
        });
        isMoving = false;
      });
      isMoving = true;
    }
  });
});

